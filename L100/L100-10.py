# 10. Conditions IF/ELSE : gérer des situations différentes
# Verification si nb est positif ou négatif / egal à 0.
nb = -1
if nb >= 0:
    print("Number is positive")
else  :
    print("Number is negative")

# Verification si nb est différent de 0.
nb = 10
if nb != 0:
    print('Number is NOT "0".')
else  :
    print('Number is "0".')

# Verification si nb est egal à 0.
nb = 0
if nb == 0:
    print('Number is "0".')
else  :
    print('Number is NOT "0".')

